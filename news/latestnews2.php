                        <h4>WAEC Cancels Results of 4,591 Candidates</h4>
                        <p>Due to various forms of malpractice, the West African Examinations Council (WAEC) has cancelled the results of 4,591 candidates who sat for the 2024 West African Senior School Certificate Examination (WASSCE).</p>

                        <h4>Details of Malpractice</h4>
                        <p>Among the affected candidates, 4,108 had their subject results cancelled for bringing unauthorised materials into the exam hall, while 483 lost their entire results for using mobile phones during the exams. Additionally, WAEC has withheld the results of 990 candidates, 781 for specific subjects and 209 for their entire results pending further investigations.</p>

                        <h4>Collusion Allegations</h4>
                        <p>Allegations of collusion involving 319 schools are also under review. The Ghana Examinations Committee approved these decisions during its 37th meeting on December 16, 2024.</p>

                        <h4>Investigation Outcomes</h4>
                        <p>WAEC has stated that withheld results may either be released or cancelled based on the outcomes of these investigations.</p>

                        <h4>Performance Trends</h4>
                        <p>Meanwhile, WAEC released provisional results that showed mixed performance across core subjects compared to 2023. The percentage of candidates who scored A1 to C6 in English Language dropped from 73.11% in 2023 to 69.52% in 2024.</p>

                        <p>Similarly, Integrated Science declined from 66.82% to 58.77%, and Social Studies decreased from 76.76% to 71.53%. However, Mathematics recorded an improvement, with performance increasing from 62.23% in 2023 to 66.86% in 2024.</p>

                        <h4>Participation Statistics</h4>
                        <p>This year, overall participation in the WASSCE rose by 2.66%, with 460,611 candidates sitting the exams compared to 448,856 in 2023.</p>

                        <p>Out of the total candidates, 247,657 were female, outnumbering the 212,954 males. Approximately 1% of registered candidates (4,609) were absent.</p>

                        <h4>Measures Against Fraud</h4>
                        <p>WAEC has introduced a QR code verification system to curb fraudulent activities related to results. It has also warned stakeholders to avoid engaging with fraudsters offering to alter results for a fee.</p>
                        
